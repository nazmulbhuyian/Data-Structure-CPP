// Question: Take a singly linked list as input and check if the linked list is sorted in ascending order.


// Sample Input
// Sample Output
// 1 5 6 8 9 -1
// YES
// 2 4 6 5 8 4 -1
// NO

#include <bits/stdc++.h>
using namespace std;
class Node
{
public:
    int val;
    Node *next;
    Node(int val)
    {
        this->val = val;
        this->next = NULL;
    }
};
void insert_at_tail(Node *&head, int v)
{
    Node *newNode = new Node(v);
    if (head == NULL)
    {
        head = newNode;
        return;
    }

    Node *tmp = head;
    while (tmp->next != NULL)
    {
        tmp = tmp->next;
    }
    // tmp ekhon last node e
    tmp->next = newNode;
}

int main()
{
    int val;
    Node *head = NULL;
    while (true)
    {
        cin >> val;
        if (val == -1)
            break;
        insert_at_tail(head, val);
    }
    int flag = 0;
    for (Node *i = head; i->next != NULL; i = i->next)
    {
        for (Node *j = i->next; j != NULL; j = j->next)
        {
            if (i->val > j->val)
            {
                flag = 1;
            }
        }
    }
    if(flag == 0){
        cout<<"YES";
    }else{
        cout<<"NO";
    }
    return 0;
}